from solution import solve
from util.test_runner import run_fun_solution

test_cases = [
  # 테스트 케이스 1: A=5, B=1, V=6 → (6-5)/(5-1)=0.25 올림하여 1+1 = 2일
  (
    "5 1 6\n",
    "2\n"
  ),
  # 테스트 케이스 2: A=100, B=99, V=100 → A(100) ≥ V(100)이므로 1일
  (
    "100 99 100\n",
    "1\n"
  ),
  # 테스트 케이스 3: A=5, B=1, V=100 → (100-5)=95, (5-1)=4, 95/4=23.75 → ceil=24, +1 = 25일
  (
    "5 1 100\n",
    "25\n"
  ),
  # 테스트 케이스 4: A=5, B=3, V=100 → (100-5)=95, (5-3)=2, 95/2=47.5 → ceil=48, +1 = 49일
  (
    "5 3 100\n",
    "49\n"
  ),
  # 테스트 케이스 5: A=100, B=99, V=1000000000 → (1000000000-100)=999999900, (100-99)=1, 999999900+1 = 999999901일
  (
    "100 99 1000000000\n",
    "999999901\n"
  ),
  # 테스트 케이스 6: A=7, B=2, V=100 → (100-7)=93, (7-2)=5, 93/5=18.6 → ceil=19, +1 = 20일
  (
    "7 2 100\n",
    "20\n"
  ),
  # 테스트 케이스 7: A=10, B=9, V=1000000000 → (1000000000-10)=999999990, (10-9)=1, 999999990+1 = 999999991일
  (
    "10 9 1000000000\n",
    "999999991\n"
  ),
  # 테스트 케이스 8: A=8, B=3, V=120 → (120-8)=112, (8-3)=5, 112/5=22.4 → ceil=23, +1 = 24일
  (
    "8 3 120\n",
    "24\n"
  ),
  # 테스트 케이스 9: A=15, B=5, V=50 → (50-15)=35, (15-5)=10, 35/10=3.5 → ceil=4, +1 = 5일
  (
    "15 5 50\n",
    "5\n"
  ),
  # 테스트 케이스 10: A=100, B=1, V=1000 → (1000-100)=900, (100-1)=99, 900/99≈9.09 → ceil=10, +1 = 11일
  (
    "100 1 1000\n",
    "11\n"
  ),
]


def test():
  for input_data, expected_output in test_cases:
    output = run_fun_solution(input_data, solve)
    assert output == expected_output, f"Input: {input_data!r} Expected: {expected_output!r}, Got: {output!r}"
